from __future__ import division

from psychopy import visual, event, core
from psychopy.visual import ShapeStim
import time

win = visual.Window(size=[400,400],units="pix",color=(1,1,1))

circle = visual.Circle(win, units="pix", radius=150, fillColor='orange', lineColor='black')
circle.draw()

rechthoek = visual.Rect(win, units="pix",width=30, height=60, fillColor='green', lineColor='black', pos=(0,150))
rechthoek.draw()

oogl = visual.Circle(win, units="pix", radius=30, fillColor='black', lineColor='black', pos=(-65, 45))
oogl.draw()

oogr = visual.Circle(win, units="pix", radius=30, fillColor='black', lineColor='black', pos=(65, 45))
oogr.draw()

mond = visual.Rect(win, units="pix", width=110, height=30, fillColor='black', lineColor='black', pos=(0, -75))
mond.draw()

Text = visual.TextStim(win, text = "Happy Halloween", color ='black', pos=(0, -175))
Text.draw()

win.flip()
