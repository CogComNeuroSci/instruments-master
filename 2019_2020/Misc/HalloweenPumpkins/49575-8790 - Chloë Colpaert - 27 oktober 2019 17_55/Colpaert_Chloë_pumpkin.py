from psychopy import visual
import time
from psychopy import core, event
#pumpkinshape
win=visual.Window(fullscr=bool,color='black')
#stem
stemvert=[(-0.2,0.5),(0,0.9),(0.3,0.4)]
stem=visual.ShapeStim(win, vertices=stemvert, lineColor=[0.420,-0.200,-0.561],fillColor=[0.976,-0.106,-0.725])
stem.draw()
#pumpkinshape
pumpkin= visual.Circle(win, radius=0.6,edges=30,lineColor = [0.420,-0.200,-0.561],fillColor='orange')
pumpkin.draw()
#mouth
mouthvert=[(-0.2,-0.1),(0,-0.3),(0.2,-0.1)]
mouth=visual.ShapeStim(win,vertices=mouthvert,lineColor='yellow',fillColor='black')
mouth.draw()
#eyes1
eyesvert=[(-0.4,0.1),(-0.3,0.2),(-0.2,0.1)]
eyes=visual.ShapeStim(win,vertices=eyesvert,lineColor='yellow',fillColor='black')
eyes.draw()
#eyes2
eyesvert=[(0.4,0.1),(0.3,0.2),(0.2,0.1)]
eyes=visual.ShapeStim(win,vertices=eyesvert,lineColor='yellow',fillColor='red')
eyes.draw()
#pumpkinlines
pumpkinline=visual.Circle(win,radius=0.6,edges=30,lineColor='yellow')
pumpkinline.draw()
linevert=[(-0.4,0.44),(-0.4,-0.44)]
line1=visual.ShapeStim(win,vertices=linevert,lineColor='yellow')
line1.draw()
#line2
linevert2=[(0,-0.3),(0,-0.6)]
line2=visual.ShapeStim(win,vertices=linevert2,lineColor='yellow')
line2.draw()
#line2.2
linevert22=[(0,0.6),(0,-0.1)]
line22=visual.ShapeStim(win,vertices=linevert22,lineColor='yellow')
line22.draw()
#line3
linevert3=[(0.4,0.44),(0.4,-0.44)]
line3=visual.ShapeStim(win,vertices=linevert3,lineColor='yellow')
line3.draw()
#texthappyhalloween
texthappyhal=visual.TextStim(win,text='happy halloween!',color=[0.459,0.294,-0.396],italic= True,pos= (0,-0.8))
texthappyhal.draw()
